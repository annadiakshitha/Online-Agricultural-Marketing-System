def t(key, lang="en"):
    """Get translation for a key in given language. Falls back to English."""
    entry = TRANSLATIONS.get(key, {})
    return entry.get(lang) or entry.get("en", key)


def get_all_for_lang(lang):
    """Return dict of all translations for a given language."""
    return {k: t(k, lang) for k in TRANSLATIONS}
